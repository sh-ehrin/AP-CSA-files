m = -2;
n = 3;
k = -10;
j = 8;

m*=k;
j += n;
m/=n;
k -= 4;
n/=10;
n*=10;
System.out.println(j+"\n" + k + "\n" + m + "\n" + n);
        int x = 3;
        int y = 4, z = 5;
        x+=2;
        y-=z;
